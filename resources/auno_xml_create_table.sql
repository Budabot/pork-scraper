CREATE TABLE auno_history (
    name VARCHAR(50),
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    breed VARCHAR(20),
    gender VARCHAR(10),
    profession VARCHAR(20),
    level SMALLINT,
    defender_rank SMALLINT,
    faction VARCHAR(10),
    org_name VARCHAR(50),
    org_rank_name VARCHAR(20),
    date DATE,
    dimension SMALLINT
);